abstract public class Enemy extends Life{
  double elapsedPlayerInsideEnemy;
  boolean firstCollision;
  public Enemy(int health, int x, int y){
    super (health, x, y);
    elapsedPlayerInsideEnemy=0;
    firstCollision = true;
  }
  
  abstract public void moveUp();
  abstract public void moveDown();
  abstract public void moveLeft();
  abstract public void moveRight();
  abstract public void takeDamage();
}